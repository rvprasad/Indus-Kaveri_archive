package edu.ksu.cis.bandera.staticanalyses.flow;

import ca.mcgill.sable.soot.jimple.Value;


/**
 * This class represents a variant of an AST node.  Created: Tue Jan 22 13:05:04 2002
 *
 * @author <a href="http://www.cis.ksu.edu/~rvprasad">Venkatesh Prasad Ranganath</a>
 * @version $Revision$
 */
public class ASTVariant
  extends AbstractValuedVariant {
	/**
	 * The <code>Value</code> object represented by this variant.
	 */
	public final Value _AST;

	/**
	 * Creates a new <code>ASTVariant</code> instance.
	 *
	 * @param v the value represented by this variant.
	 * @param node the flow graph node that summarizes the values for this variant.
	 */
	protected ASTVariant(Value v, IFGNode node) {
		super(node);
		this._AST = v;
	}
}

